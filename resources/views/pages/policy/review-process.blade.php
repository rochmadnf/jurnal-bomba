                href="{{ route('peer-reviewer') }}">mitra bestari</a> <em>(double blind peer review)</em>.
            Tanggapan dari para reviewer ini akan dijadikan landasan bagi Editor
            untuk menentukan apakah suatu artikel dapat diterima <em>(accepted)</em>, diterima apabila direvisi
            <em>(accepted with major/minor revision)</em>, atau ditolak <em>(rejected)</em>.
        </p>
        <p class="text-justify">
            Suatu artikel ditolak untuk dimuat
            disebabkan oleh berbagai pertimbangan, diantara artikel tidak sesuai dengan ruang lingkup,
            sisi ilmiah tulisan tersebut kurang memadai untuk digolongkan dalam suatu tulisan ilmiah,
            kesalahan metodologi yang mendasar, atau dikarenakan penulis menolak untuk melakukan
            saran perbaikan yang diberikan oleh <a href="{{ route('peer-reviewer') }}">mitra bestari</a> tanpa dasar yang
            logis. Semua artikel yang
            dikirim akan diperiksa unsur plagiasinya menggunakan perangkat lunak <em>(software)</em> anti
            plagiarisme <a target="_blank" href="https://turnitin.com">Turnitin</a> dan <a target="_blank"
                href="https://www.copyscape.com">Copy Scape</a>.
